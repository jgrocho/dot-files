# Some more general purpose bash aliases.
alias ll='ls -alF'
alias la='ls -A'
alias l='ls -CF'

# Without vowels are quiet
alias pshd='>/dev/null pushd'
alias ppd='>/dev/null popd'

# Make less behave like cat for one screen of text
alias less='less -FRX'

alias rot13="tr '[A-Za-z]' '[N-ZA-Mn-za-m]'"
